package com.example.Tarea2_Isoft.Model;

public class Auto {

    private int idAuto;
    private String marcaAuto;
    private int anioAuto;
    private String colorAuto;
    private double precioAuto;
    private boolean turbo;
    private String tipoAuto;
    private String motorAuto;
    private int cabina;
    private boolean sunroof;
    private int popularidad;

    public Auto(){

    }

    public int getIdAuto() {
        return idAuto;
    }

    public void setIdAuto(int idAuto) {
        this.idAuto = idAuto;
    }

    public String getMarcaAuto() {
        return marcaAuto;
    }

    public void setMarcaAuto(String marcaAuto) {
        this.marcaAuto = marcaAuto;
    }

    public int getAnioAuto() {
        return anioAuto;
    }

    public void setAnioAuto(int anioAuto) {
        this.anioAuto = anioAuto;
    }

    public String getColorAuto() {
        return colorAuto;
    }

    public void setColorAuto(String colorAuto) {
        this.colorAuto = colorAuto;
    }

    public double getPrecioAuto() {
        return precioAuto;
    }

    public void setPrecioAuto(double precioAuto) {
        this.precioAuto = precioAuto;
    }

    public boolean isTurbo() {
        return turbo;
    }

    public void setTurbo(boolean turbo) {
        this.turbo = turbo;
    }

    public String getTipoAuto() {
        return tipoAuto;
    }

    public void setTipoAuto(String tipoAuto) {
        this.tipoAuto = tipoAuto;
    }

    public String getMotorAuto() {
        return motorAuto;
    }

    public void setMotorAuto(String motorAuto) {
        this.motorAuto = motorAuto;
    }

    public int getCabina() {
        return cabina;
    }

    public void setCabina(int cabina) {
        this.cabina = cabina;
    }

    public boolean isSunroof() {
        return sunroof;
    }

    public void setSunroof(boolean sunroof) {
        this.sunroof = sunroof;
    }

    public int getPopularidad() {
        return popularidad;
    }

    public void setPopularidad(int popularidad) {
        this.popularidad = popularidad;
    }
}
